package ModelDAO;

public class Disciplina {
	
	protected Double id;
	protected String nome;
	protected Double idsemestre;
	
	
	public Double getId() {
		return id;
	}
	public void setId(Double id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Double getIdSemestre() {
		return idsemestre;
	}
	public void setIdSemestre(Double idsemestre) {
		this.idsemestre = idsemestre;
	}

	
	
	

}
